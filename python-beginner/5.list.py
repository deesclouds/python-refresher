# making an array

friends = ['Molly', 'Hercules', 'Boo', 'Oscar', 'Toby']

print(friends)

print(friends[2])
print(friends[0])

# indexing starts at 0

print(friends[-1]) # prints the item at the end of the list
print(friends[1:]) # prints items from the 1 index and the rest
print(friends[1:3]) # prints the items from index 1 to 3 but not the item within the 3 index

friends[1] = "Mike" #updates the value in index one with a new value
print(friends)

